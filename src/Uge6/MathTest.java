package Uge6;

public class MathTest {
        public static void main(String[] args) {
            // 1. Math.random(): Tilfældig værdi mellem 0.0 og 1.0
            System.out.println("Math.random(): " + Math.random());

            // 2. Math.abs(x): Absolut værdi af et tal
            System.out.println("Math.abs(-25): " + Math.abs(-25));

            // 3. Math.pow(a, b): a opløftet til b'te potens
            System.out.println("Math.pow(2, 3): " + Math.pow(2, 3));

            // 4. Math.sqrt(x): Kvadratrod af et tal
            System.out.println("Math.sqrt(49): " + Math.sqrt(49));

            // 5. Math.max(a, b): Største af to tal
            System.out.println("Math.max(10, 20): " + Math.max(10, 20));

            // 6. Math.min(a, b): Mindste af to tal
            System.out.println("Math.min(10, 20): " + Math.min(10, 20));

            // 7. Math.round(x): Afrunder til nærmeste heltal
            System.out.println("Math.round(5.6): " + Math.round(5.6));

    }
}
